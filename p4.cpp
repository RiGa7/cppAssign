#include <iostream>

using namespace std;

int main() {
	
	int num;
	
	cout<<"Enter the number: "<<endl;
	cin>>num;
	
	cout<<"Value of num is : "<<num<<endl;
	cout<<"After post increment value is : "<<num++<<endl;
	cout<<"Value of num is : "<<num<<endl;
	cout<<"After pre increment value is : "<<++num<<endl;
	cout<<"Value of num is : "<<num<<endl;
	
	return 0;	

}

